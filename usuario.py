    def __init__(self, nombre,apellido,telefono,direccion,password,id,correo,rol):
        self.nombre = nombre
        self.apellido = apellido
        self.telefono = telefono
        self.direccion = direccion
        self.password = password
        self.id = id
        self.correo = correo
        self.rol = rol